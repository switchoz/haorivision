async function seed() {
  await mongoose.connect(MONGO_URI);
  console.log("Connected to MongoDB");

  // Blog posts
  const existingBlogs = await BlogPost.countDocuments();
  if (existingBlogs === 0) {
    await BlogPost.insertMany(blogPosts);
    console.log(`Inserted ${blogPosts.length} blog posts`);
  } else {
    console.log(`Blog posts already exist (${existingBlogs}), skipping`);
  }

  // Reviews
  const existingReviews = await Review.countDocuments();
  if (existingReviews === 0) {
    await Review.insertMany(reviews);
    console.log(`Inserted ${reviews.length} reviews`);
  } else {
    console.log(`Reviews already exist (${existingReviews}), skipping`);
  }

  await mongoose.disconnect();
  console.log("Done");
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
