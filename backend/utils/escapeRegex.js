/**
 * Экранирует спецсимволы регулярных выражений в строке.
 * Предотвращает ReDoS-атаки при использовании пользовательского ввода в $regex / new RegExp.
 * @param {string} str - Строка для экранирования
 * @returns {string} Экранированная строка, безопасная для использования в RegExp
 */
export const escapeRegex = (str) => str.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
